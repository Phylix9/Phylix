package com.Phylix;

public class Ejercicio {
    private String nombre;
    private String detalle;

    public Ejercicio(String nombre, String detalle) {
        this.nombre = nombre;
        this.detalle = detalle;
    }

    public String getNombre() {
        return nombre;
    }

    public String getDetalle() {
        return detalle;
    }
}
